<p>
  In order for bug reports to be useful for the ArcOS developers, we collect
  some required data to see where and how the bug took place.
</p>
<h4>This information includes:</h4>
<ul>
  <li>The ArcOS Log (without API and AuthCode);</li>
  <li>The version of ArcOS and its mode;</li>
  <li>Your User Agent string;</li>
  <li>The frontend URL;</li>
  <li>and the development metadata.</li>
</ul>
<p>
  If you choose to allow it we can also collect your <i>ArcAPI hostname</i> and your
  preferences.
</p>
